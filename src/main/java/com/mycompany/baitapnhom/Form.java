    public void hienThiDuLieu(){
        try{
            List<SinhVien> list = SinhVien.layDanhSachSinhVien();
            DefaultTableModel model = new DefaultTableModel(
                new Object [][] {
                },
                new String [] {
                    "Mã sinh viên", "Họ tên", "Lớp", "GPA"
                }
            );
            for(SinhVien sinhVien:list){
                Object[] row= new Object[4];
                row[0] = sinhVien.getMaSv();
                row[1] = sinhVien.getHoTen();
                row[2] = sinhVien.getLop();
                row[3] = sinhVien.getGpa();
                model.addRow(row);
            }
            bangThongTinSinhVien.setModel(model);
        } catch(SQLException exception){
            exception.printStackTrace();
            JOptionPane.showMessageDialog(null, "Có lỗi xảy ra khi lấy dữ liệu.");
        }
    }
    
