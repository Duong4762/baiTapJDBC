    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO them
        try{
            String maSv = fieldMaSv.getText();
            String hoTen = fieldHoTen.getText();
            String lop = fieldLop.getText();
            double gpa = Double.parseDouble(fieldGpa.getText());
            SinhVien sinhVien = new SinhVien(maSv, hoTen, lop, gpa);
            SinhVien.themSinhVien(sinhVien);
        } catch(SQLException exception){
            exception.printStackTrace();
            JOptionPane.showMessageDialog(null, "Có lỗi xảy ra khi thêm dữ liệu.");
        } catch(NumberFormatException exception){
            exception.printStackTrace();
            JOptionPane.showMessageDialog(null, "Gpa phải là số thực.");
        }
        hienThiDuLieu();
    }//GEN-LAST:event_jButton2ActionPerformed

